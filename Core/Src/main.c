void FLASH_If_Write(uint32_t* flash_addr ,uint32_t *data,uint32_t data_length)
{
	/* Unlock the Flash to enable the flash control register access *************/
	HAL_FLASH_Unlock();

	HAL_FLASH_OB_Unlock();

	volatile uint32_t write_cnt=0, index=0;

	volatile HAL_StatusTypeDef status;
	status = HAL_OK;
	while(index < data_length)
	{
		if (status == HAL_OK)
		{
			//memset(data,0xAA55AA55,32);
			status = HAL_FLASH_Program(FLASH_TYPEPROGRAM_FLASHWORD, (*flash_addr), (uint32_t )data);
			if(status == HAL_OK)
			{
				*flash_addr = (*flash_addr)+32;//Destination address
				data = data+8;                      //Source address
				index+=32;
			}
		}
	}
	HAL_FLASH_OB_Lock();
	HAL_FLASH_Lock();
}

